public class Felig {
    public String nombre;
    public int cedula;
    public double monto;
    public Felig(String nombre, int cedula, double monto){
        this.nombre = nombre;
        this.cedula = cedula;
        this.monto = monto;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getCedula() {
        return cedula;
    }

    public void setCedula(int cedula) {
        this.cedula = cedula;
    }

    public double getMonto() {
        return monto;
    }

    public void setMonto(double monto) {
        this.monto = monto;
    }
    
}
